package charge;


public class chargeVO {
	private int noticeboard_num;
	private String charge_Juris;
	private String charge_area;
	private String charge_departname;
	private String charge_person;
	private String charge_num;
	private String charge_fax;
	
	public int getNoticeboard_num() {
		return noticeboard_num;
	}
	public void setNoticeboard_num(int noticeboard_num) {
		this.noticeboard_num = noticeboard_num;
	}
	public String getCharge_Juris() {
		return charge_Juris;
	}
	public void setCharge_Juris(String charge_Juris) {
		this.charge_Juris = charge_Juris;
	}
	public String getCharge_area() {
		return charge_area;
	}
	public void setCharge_area(String charge_area) {
		this.charge_area = charge_area;
	}
	public String getCharge_departname() {
		return charge_departname;
	}
	public void setCharge_departname(String charge_departname) {
		this.charge_departname = charge_departname;
	}
	public String getCharge_person() {
		return charge_person;
	}
	public void setCharge_person(String charge_person) {
		this.charge_person = charge_person;
	}
	public String getCharge_num() {
		return charge_num;
	}
	public void setCharge_num(String charge_num) {
		this.charge_num = charge_num;
	}
	public String getCharge_fax() {
		return charge_fax;
	}
	public void setCharge_fax(String charge_fax) {
		this.charge_fax = charge_fax;
	}
}
